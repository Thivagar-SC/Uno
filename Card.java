public class Card
{
    private static int cardFaceValue;  //value of card
    private int colour; //color of card

    public Card(int cardFaceValue, int colour)
    {
        this.cardFaceValue = cardFaceValue;
        this.colour = colour;
    }

    public int getValue()
    {
        return getValue();
    }

    public int getColour()
    {
        return getColour();
    }

}
